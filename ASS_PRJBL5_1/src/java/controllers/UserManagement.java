    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        UserDAO da = new UserDAO();
        if (request.getParameter("mode") != null && request.getParameter("mode").equals("updateUser")) {
            String id = request.getParameter("id");
            user u = da.getUserId(id);

            request.setAttribute("u", u);
            request.getRequestDispatcher("UpdateUser.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        String fullname = request.getParameter("name");
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String phone = request.getParameter("phone");
        String submit = request.getParameter("submit");
        UserDAO dao = new UserDAO();
        if (submit.equals("BACK TO LOGIN")) {
            response.sendRedirect("Login.jsp");
        }else{
            dao.UpdateUserManagement(id, fullname, username, password, phone);

        user u = dao.getUserId(id);
        request.setAttribute("u", u);
        request.getRequestDispatcher("userManagement.jsp").forward(request, response);
        }
        
    }

